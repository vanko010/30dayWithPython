# Introduction
# Day 1 - 30DaysOfPython Challenge
print(2 + 3)   # addition(+) -- Cộng
print(3 - 1)   # subtraction(-) -- Trừ
print(2 * 3)   # multiplication(*) -- Nhân 
print(3 / 2)   # division(/) -- Chia
print(3 ** 2)  # exponential(**) -- Số mũ
print(3 % 2)   # modulus(%) -- Chia lấy phần dư
print(3 // 2)  # Floor division operator(//) -- Chia lấy phần nguyên

# Checking data types -- Kiểm tra các loại dữ liệu

print(type(10))                  # Int -- Số nguyên
print(type(3.14))                # Float -- Số thực
print(type(1 + 3j))              # Complex -- Số phức
print(type('Asabeneh'))          # String -- Chuỗi
print(type([1, 2, 3]))           # List -- Danh sách
print(type({'name':'Asabeneh'})) # Dictionary -- Lưu trữ dưới dạng cặp khóa - Giá trị (Key-Value)
print(type({9.8, 3.14, 2.7}))    # Set Tập hợp 
print(type((9.8, 3.14, 2.7)))    # Tuple Lưu trữ đối tượng không thay đổi (Như hằng số)
